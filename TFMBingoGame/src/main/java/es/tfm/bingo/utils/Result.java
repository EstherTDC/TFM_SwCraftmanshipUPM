package es.tfm.bingo.utils;

public enum Result {
	OK("OK"),
	GAME_NOT_FOUND("GAME NOT FOUND"),
	GAME_ALREADY_STARTED("GAME ALREADY STARTED"),
	GAME_NOT_RUNNING("GAME NOT RUNNING"),
	PLAYER_NOT_FOUND("PLAYER NOT FOUND"),
	PLAYER_ALREADY_EXISTS_FOR_THIS_GAME("PLAYER ALREADY EXISTS FOR THIS GAME"),
	GAME_ALREADY_EXISTS("GAME ALREADY EXISTS"),
	PLAYER_DOES_NOT_BELONG_TO_GAME("PLAYER DOES NOT BELONG TO GAME"),
	NO_MINIMUM_NUMBER_OF_PLAYERS_IN_GAME("NO MINIMUM NUMBER OF PLAYERS IN GAME"),
	NON_EXISTING_BINGO_TYPE("NON EXISTING BINGO TYPE"),
	GAME_ONGOING_CANNOT_BE_DELETED("GAME ONGOING. CANNOT BE DELETED"),
	GAME_ALREADY_INITITATED_CANNOT_JOIN("GAME ALREADY INITITATED CANNOT JOIN"),
	INVALID_REQUESTED_NUMBER_OF_CARDS("INVALID REQUESTED NUMBER OF CARDS");


    private final String value;
    
    private Result(String value) {
        this.value = value;
    }

    public String get() {
        return this.value;
    }
    
    public boolean equals(String valueToCheck) {
    	return this.value.equals(valueToCheck);
    }
}
